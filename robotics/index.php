								<h3>Who we are.</h3>
								<p>We grew from a group of about a dozen students crammed inside of a Cal-Poly Pomona study room that orginally met during Fridays, and Sundays. The team was an activity separated from school, so we got used to being self-sufficient and we grew closer as a group. Apart from Cal-Poly mentors we created a system where the club would be managed by Oversight Executives (team members with executive responsibilities to the team). From there, the team was departmentalized between Programming and Mechanical Design Departments.
                                <p>We understood that we didn't need the support of our school to follow our heart. So for a few months, we just went with the flow. Our rookie season brought us in front several opportunities which enabled our team to grow even further. Today as for every tomorrow, we hope to bring that same motivation, and success as we expand our team by participating in more events, outreach activities, and inspire new students to join our team.</p>
                                <h3>From past to present . . .</h3>
                               <p>The iPoly Robotics team is a collection of students with a burning passion to pursue careers in the field of STEM. The group was originally formed in September of 2013 by the collaborative efforts of Cal-Poly and iPoly students. The group started wthout the initial support of the iPoly Administration and it was the result of pure student perserverance which led the team to success. While under the guidance of Cal-Poly engineering mentors the team competed in FIRST (For Inspiration as Recognition of Science and Technology) events under the aliases Inland Empire Broncos, the League of iPoly, and more commonly FTC (FIRST Tech Challenge) Team 7528.<p>
                               <p>After the team's rookie season concluded with an award-winning finish, the team achieved an official affiliation with International Polytechnic High School (iPoly). As a new member of the iPoly family the team has undergone a name change to incorporate the iPoly name. The team's strategy meetings were also relocated to iPoly for added convenience.<p>
								<p>Currently, the team is managed by members of Oversight which comprise of student officers who have the following titles: CEO (Chief Executive Officer), COO (Chief of Operations Officer), CFO (Chief Financial Officer), CPO (Chief Procurement Officer), CSO (Chief of Science Officer) and chief secretary. Beyond Oversight, the team is departmentalized into Mechanical Design, Public Relations and Programming Departments with individual presidents. To the iPoly administration, the team is represented by advisors Dale Hunzeker and Tony Pang.<p>
								<p>Created by students from the classes of 2014, 2015, and 2017, the iPoly Robotics team is designed to create an environment for members to present hands-on learning and problem solving skills throughout the greater Southern California region.<p>
                                <p>Founding members include: Mahmood Baggia ('17), Sara Baggia ('15), Carlos Cerrillos ('14), Alejandro Espinoza ('14), Adrian Galvan ('17), Carl La Russa ('15), Maryela Mendoza ('17), Noah Nakielski ('14), Regina Ongowarsito ('14), Bryan Posada ('14), Miguel Samonte ('14), Brendan Trang ('14), and Marina Williams ('17).<p>
